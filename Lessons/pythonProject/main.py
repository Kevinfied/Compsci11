# Variables
# A variable is a named spot in memory that holds a value

# When you make a variable it should have a name that makes it clear what it logically hold.

# Python's variable name rules
# must start with a letter (or _), then contain  letters, numbers or underscores.

# e.g. good variable names
# age
# numPies (camel case)
# last_name (Snake Case)

# Bad Variable names:
# 4ever     - starts with number
# q      - hard to comprehend
# name$$$      - no symbols

# sometimes okay
# x, y ...      - can be okay based on context